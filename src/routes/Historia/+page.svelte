Från damjunior till senior
Vi erbjuder en välorganiserad föreningsmiljö med riktigt bra förutsättningar till träning. <br> <br> Vi vill hjälpa spelare att utvecklas och förenkla steget från junior till senior i samma förening. Spelare som är redo för spel i högre division än vi erbjuder ska få stöttning i processen att gå vidare på samma vis som vi själva, för var tid, vill spela på en nivå som speglar lagets slagkraftighet.

Umeå Västra familjen
Västra har årligen mellan 65-80 spelare fördelat på 3-4 träningsgrupper och 4-5 matchlag där de yngsta spelarna är 15 år. Vi har en röd tråd genom hela föreningen rörande såväl fys, innebandyträningar och spelidé. <br> <br> Vår trappa börjar från damutveckling vidare till juniorallsvenskan och våra lag i division 1.

Som förening vill vi erbjuda de bästa förutsättningarna för att nå långsiktig framgång.

Föreningens fystränare finns tillgänglig för att stötta alla spelare i föreningen och hjälpa dem att nå våra fysmål. Under försäsongen kör hela föreningen gemensamt oavsett lagtillhörighet och genom regelbundna fystester säkerställer vi att dubbelidrottande spelare inte hamnar efter via sina alternativa träningsupplägg.

